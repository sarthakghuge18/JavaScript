

// loop are used to execute peice of code again and again 

// for loop 


//printing hello world 10 times
for(let i = 0 ; i < 10 ; i ++ ){
    console.log("hello world");
}


//printing the number from 0 to 10
for(let count = 0 ; count <= 10 ; count ++){
    console.log(count);
}

//calculate sum from 0 to 10
let sum = 0;
for(let i = 0 ; i <= 10 ; i ++){
    sum = sum + i;
}
console.log("sum : ", sum);

